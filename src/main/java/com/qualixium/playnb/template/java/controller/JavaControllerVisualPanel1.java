    private void myOwnInitComponents() {
        Project project = Templates.getProject(wizard);
        JTextComponent comp = (JTextComponent) cbxPackageName.getEditor().getEditorComponent();
        comp.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                updateCreatedFileText();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateCreatedFileText();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                updateCreatedFileText();
            }
        });

        txtProjectName.setText(((PlayProject) project).getProjectName());

        txtName.setText("NewController");

        FileObject foTargetFolder = Templates.getTargetFolder(wizard);
        String currentPackageName = foTargetFolder.getPath()
                .replace(project.getProjectDirectory().getPath() + "/app", "")
                .replace(project.getProjectDirectory().getPath() + "/test", "")
                .replace("/", ".");

        if (currentPackageName.startsWith(".")) {
            currentPackageName = currentPackageName.substring(1);
        }

        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        List<String> packagesName = TemplateUtil.getPackagesName(project.getProjectDirectory().getPath() + "/app");
        packagesName.stream().forEach((packageName) -> {
            model.addElement(packageName);
        });

        cbxPackageName.setModel(model);
        cbxPackageName.setSelectedItem(currentPackageName);

        txtName.requestFocus();
        txtName.selectAll();
    }

