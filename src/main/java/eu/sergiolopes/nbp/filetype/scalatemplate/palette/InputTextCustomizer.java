    @NbBundle.Messages({"LBL_Customizer_InsertPrefix_InputText=Insert inputText", "NAME_Play-InputText=Input Text"})
    public boolean showDialog() {
        dialogOK = false;
        descriptor = new DialogDescriptor(this, Bundle.LBL_Customizer_InsertPrefix_InputText(), true, DialogDescriptor.OK_CANCEL_OPTION, DialogDescriptor.OK_OPTION, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (descriptor.getValue().equals(DialogDescriptor.OK_OPTION)) {
                    evaluateInput();
                    dialogOK = true;
                }
                dialog.dispose();
            }
        });
        dialog = DialogDisplayer.getDefault().createDialog(descriptor);
        dialog.setVisible(true);
        return dialogOK;
    }

    private void evaluateInput() {
        HTMLPaletteUtilities.PREFIX_WITH_HELPER = chPrefixWithHelper.isSelected();
        HTMLPaletteUtilities.MY_FORM_VARIABLE_NAME = txtFormVariableName.getText();
        inputText.formVariableName = txtFormVariableName.getText();
        inputText.name = txtName.getText();
        inputText.id = txtId.getText();
        inputText.label = txtLabel.getText();
        inputText.clazz = txtClass.getText();
        if (ftfMaxLength.getText().isEmpty()) {
            inputText.maxLength = 0;
        } else {
            inputText.maxLength = Integer.valueOf(ftfMaxLength.getText());
        }
        inputText.size = (int) spSize.getValue();
        inputText.disabled = chDisabled.isSelected();
        inputText.readOnly = chReadOnly.isSelected();
        inputText.required = chRequired.isSelected();
        inputText.autocomplete = chAutocomplete.isSelected();
    }

    private void myOwnInitComponents() {
        chPrefixWithHelper.setSelected(HTMLPaletteUtilities.PREFIX_WITH_HELPER);
        txtClass.setText(inputText.clazz == null ? "" : inputText.clazz);
        txtFormVariableName.setText(HTMLPaletteUtilities.MY_FORM_VARIABLE_NAME);
        txtFormVariableName.selectAll();
    }

